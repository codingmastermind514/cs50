# prompting the user to give input for m or mass
m = int(input('m: '))

# assigning a value to a variable named c (speed of light)
c = 300000000

# the variable e (energy) is assigned to the value of m multiplied by c squared (c**2)
e = m*(c**2)

# printing out the result using a formatted string which concatnates all datatypes
print(f'E: {e}')
